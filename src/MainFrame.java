    private void dropReservationSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropReservationSubmitButtonActionPerformed
        // TODO add your handling code here:
        try {
            String name = dropReservationNameCombobox.getSelectedItem().toString();
            Date day = Date.valueOf(dropReservationDateCombobox.getSelectedItem().toString());
            
            dropReservationStatusLabel.setForeground(Color.BLACK);
            String statusText = "<html>Reservation for " + name + " on " + day + " has been removed from the system.";
            ReservationEntry entry = ReservationQueries.cancelReservationEntry(name, day);
            if(entry != null) {
                statusText += "<br>" + entry.getRoom() + " has been reserved for " + entry.getName() + " on " + entry.getDate();
            }
            dropReservationStatusLabel.setText(statusText);
        } catch(Exception e) {
            //catches error from addRoom sets different status message
            dropReservationStatusLabel.setForeground(Color.RED);
            dropReservationStatusLabel.setText("No reservation has been removed from the system.");
        }
        setStatusLabels();
    }//GEN-LAST:event_dropReservationSubmitButtonActionPerformed

