    private void addReservationSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addReservationSubmitButtonActionPerformed
        //Reserves room for faculty member on given date
        //Retrieve given information
        String facultyName = addReservationNameCombobox.getSelectedItem().toString();
        Date day = Date.valueOf(addReservationDateCombobox.getSelectedItem().toString());
        int seats = Integer.parseInt(reservationSeatField.getText());
        RoomEntry room = RoomQueries.reserveRoom(seats, day);
        Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        

        if(room == null) {
            WaitlistEntry entry = new WaitlistEntry(facultyName, day, seats, timestamp);
            try {
                WaitlistQueries.addWaitlistEntry(entry);
            } catch (SQLIntegrityConstraintViolationException ex) {
                //If a duplicate key is entered
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            addReservationStatusLabel.setText("No room found: " + facultyName + " has been waitlisted for " + day);
        } else {
            ReservationEntry entry = new ReservationEntry(facultyName, room.getName(), day, seats, timestamp);
            try {
                ReservationQueries.addReservationEntry(entry);
            } catch (SQLIntegrityConstraintViolationException ex) {
                //If a duplicate key is entered
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            addReservationStatusLabel.setText(facultyName + " has successfully reserved room " + room.getName() + " for " + day);
        }
        setStatusLabels();
    }//GEN-LAST:event_addReservationSubmitButtonActionPerformed

